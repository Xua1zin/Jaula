    private void jBFormularioConcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBFormularioConcluirMouseClicked
        try {
            CadastroFormularioDao cadastroDao = new CadastroFormularioDao();
            ArrayList<CadastroFormularioModel> listaCadastros = cadastroDao.consultar();
            int linha = -1;
            String formID = jTFormularioID.getText();
            for (int i = 0; i < listaCadastros.size(); i++) {
                CadastroFormularioModel cadastro = listaCadastros.get(i);
                if (formID.equals(String.valueOf(cadastro.getFormID()))) {
                    linha = i;
                    break;
                }
            }

            CadastroFormularioModel cadastroSelecionado = listaCadastros.get(linha);
            String status = cadastroSelecionado.getFormStatusName();

            if ("Em andamento".equals(status)) {
                cadastroSelecionado.setFormStatusName("Concluido");
                cadastroDao.alterar(cadastroSelecionado);
                JOptionPane.showMessageDialog(null, "Formulário concluido com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Exame concluido ou cancelado");
            }

            this.dispose();
            Tabelas tabelas = new Tabelas();
            tabelas.atualizaTabelaForm(listaCadastros);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao invalidar o formulário:\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBFormularioConcluirMouseClicked

