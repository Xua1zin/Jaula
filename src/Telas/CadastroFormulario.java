    private void calcularIMC() {
        try {
            String alturaText = jTFormularioAltura.getText().replaceAll("[\\ ]", "").trim();
            String pesoText = jTFormularioPeso.getText().replaceAll("[\\ ]", "").trim();
            if (!pesoText.isEmpty() && !alturaText.isEmpty()) {
                pesoText = jTFormularioPeso.getText().replace(",", ".").trim();
                alturaText = jTFormularioAltura.getText().replace(",", ".").trim();
                double peso = Double.parseDouble(pesoText);
                double altura = Double.parseDouble(alturaText);

                double imc = peso / (altura * altura);

                String classificacao;
                if (imc <= 16.9) {
                    classificacao = "Muito abaixo do peso";
                } else if (imc >= 17 && imc <= 18.4) {
                    classificacao = "Abaixo do peso";
                } else if (imc >= 18.5 && imc <= 24.9) {
                    classificacao = "Normal";
                } else if (imc >= 25 && imc <= 29.9) {
                    classificacao = "Acima do peso";
                } else if (imc >= 30 && imc <= 34.9) {
                    classificacao = "Obesidade I";
                } else if (imc >= 35 && imc <= 40) {
                    classificacao = "Obesidade II";
                } else {
                    classificacao = "Obesidade III";
                }

                jTFormularioIMC.setText(classificacao);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro!");
        }
    }

