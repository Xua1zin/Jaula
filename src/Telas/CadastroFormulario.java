    private void jBFormularioSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBFormularioSalvarMouseClicked
        try {
            CadastroFormularioDao cadastroDao = new CadastroFormularioDao();
            ArrayList<CadastroFormularioModel> listaCadastros = cadastroDao.consultar();
            int linha = -1;
            String formID = jTFormularioID.getText();

            int i;
            for (i = 0; i < listaCadastros.size(); i++) {
                CadastroFormularioModel cadastro = listaCadastros.get(i);
                if (formID.equals(String.valueOf(cadastro.getFormID()))) {
                    linha = i;
                    break;
                }
            }

            CadastroFormularioModel cadastroSelecionado;
            if (linha == -1) {
                cadastroSelecionado = new CadastroFormularioModel();
                int novoID = listaCadastros.stream()
                        .mapToInt(CadastroFormularioModel::getFormID)
                        .max()
                        .orElse(0) + 1;
                cadastroSelecionado.setFormID(novoID);
                cadastroSelecionado.setFormStatusName("Em andamento");
            } else {
                cadastroSelecionado = listaCadastros.get(linha);
            }

            cadastroSelecionado.setPatientID(Integer.parseInt(jTPacienteID.getText()));
            cadastroSelecionado.setPatientName(jTPacienteNome.getText());

            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            java.util.Date utilDateExame = dateFormat.parse(jFFormularioDtExame.getText());
            java.util.Date utilDateNascimento = dateFormat.parse(jFPacienteNascimento.getText());

            java.sql.Date sqlDateExame = new java.sql.Date(utilDateExame.getTime());
            java.sql.Date sqlDateNascimento = new java.sql.Date(utilDateNascimento.getTime());

            cadastroSelecionado.setFormExamDate(sqlDateExame);
            cadastroSelecionado.setPatientBirthdate(sqlDateNascimento);
            
            cadastroSelecionado.setSexName((String) jCPacienteSexo.getSelectedItem());
            cadastroSelecionado.setFormHeight(Double.parseDouble(jTFormularioAltura.getText()));
            cadastroSelecionado.setFormWeight(Double.parseDouble(jTFormularioPeso.getText()));
            cadastroSelecionado.setImcName(jTFormularioIMC.getText());
            cadastroSelecionado.setFormNeckWidth(Double.parseDouble(jTFormularioPescoco.getText()));
            cadastroSelecionado.setFormWaistWidth(Double.parseDouble(jTFormularioCintura.getText()));
            cadastroSelecionado.setFormObjective(jTFormularioObjetivo.getText());
            cadastroSelecionado.setFormHabits(jTAFormularioHabito.getText());
            cadastroSelecionado.setFormExercises(jTAFormularioExercicio.getText());
            cadastroSelecionado.setFormFeedingPattern(jTAFormularioAlimentacao.getText());
            cadastroSelecionado.setFormSintoms(jTAFormularioSintoma.getText());
            cadastroSelecionado.setFormUrine(jTAFormularioUrina.getText());
            cadastroSelecionado.setFormFeces(jTAFormularioFezes.getText());
            cadastroSelecionado.setFormObservation(jTAFormularioObservacao.getText());

            if (linha == -1) {
                cadastroDao.inserir(cadastroSelecionado);
                JOptionPane.showMessageDialog(null, "Novo formulário criado com sucesso.");
            } else if ("Em andamento".equals(cadastroSelecionado.getFormStatusName())) {
                cadastroDao.alterar(cadastroSelecionado);
                JOptionPane.showMessageDialog(null, "Formulário atualizado com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Somente formulários com status 'Em andamento' podem ser alterados.");
                return;
            }

            Tabelas tabelas = new Tabelas();
            tabelas.atualizaTabelaForm(cadastroDao.consultar());

            cadastroDao.vincularFormPaciente(cadastroSelecionado);

            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao salvar o formulário:\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBFormularioSalvarMouseClicked

