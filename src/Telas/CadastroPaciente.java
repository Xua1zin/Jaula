    public void preencherEnderecoPorCEP() {
        boolean isCEPEmpty = false;
        String Cep = jFPacienteCEP.getText().replaceAll("[\\ ]", "").trim();
        isCEPEmpty = Cep.isEmpty();
        if (jFPacienteCEP != null || isCEPEmpty) {
            ViaCEP viaCep = new ViaCEP();
            try {
                viaCep.buscar(jFPacienteCEP.getText());
                jTPacienteRua.setText(viaCep.getLogradouro());
                jTPacienteBairro.setText(viaCep.getBairro());
                jTPacienteCidade.setText(viaCep.getLocalidade());
                jTPacienteEstado.setText(viaCep.getUf());
            } catch (ViaCEPException ex) {
                JOptionPane.showMessageDialog(null, "Ocorrou um erro inesperado: \n" + ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void jBPacienteSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBPacienteSalvarMouseClicked
        try {
            CadastroPacienteDao cadastroDao = new CadastroPacienteDao();
            ArrayList<CadastroPacienteModel> listaCadastros = cadastroDao.consultar();
            int linha = -1;
            String pacientID = jTPacienteID.getText();

            boolean isCPFEmpty = false;
            if (jCPacienteNacionalidade.getSelectedIndex() == 26) {
                String cpf = jFPacienteCPF.getText().replaceAll("[\\.-]", "").trim();
                isCPFEmpty = cpf.isEmpty();
            }
            if ((jTPacienteID.getText().trim().isEmpty())
                    || (jTPacienteNome.getText().trim().isEmpty())
                    || (jFPacienteNascimento.getText().trim().isEmpty())
                    || isCPFEmpty
                    || (jCPacienteNacionalidade.getSelectedIndex() < 0)
                    || (jCPacienteSexo.getSelectedIndex() < 0)) {
                JOptionPane.showMessageDialog(null, "Dados Inválidos");
            } else {
                for (int i = 0; i < listaCadastros.size(); i++) {
                    CadastroPacienteModel cadastro = listaCadastros.get(i);
                    if (pacientID.equals(String.valueOf(cadastro.getPatientID()))) {
                        linha = i;
                        break;
                    }
                }

                CadastroPacienteModel cadastroSelecionado;
                if (linha == -1) {
                    cadastroSelecionado = new CadastroPacienteModel();
                    int novoID = listaCadastros.stream()
                            .mapToInt(CadastroPacienteModel::getPatientID)
                            .max()
                            .orElse(0) + 1;
                    cadastroSelecionado.setPatientID(novoID);
                } else {
                    cadastroSelecionado = listaCadastros.get(linha);
                }

                cadastroSelecionado.setPatientName(jTPacienteNome.getText());

                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date parsedDate = dateFormat.parse(jFPacienteNascimento.getText());
                Date sqlDate = new Date(parsedDate.getTime());
                cadastroSelecionado.setPatientBirthdate(sqlDate);

                String cpf = jFPacienteCPF.getText().replaceAll("[\\.-]", "");
                cadastroSelecionado.setPatientCPF(cpf);

                String rg = jFPacienteRG.getText().replaceAll("[\\.-]", "");
                cadastroSelecionado.setPatientRG(rg);

                cadastroSelecionado.setSexName((String) jCPacienteSexo.getSelectedItem());
                cadastroSelecionado.setCountryName((String) jCPacienteNacionalidade.getSelectedItem());
                cadastroSelecionado.setPatientTelefone(jTPacienteTelefone.getText());
                cadastroSelecionado.setPatientEmail(jTPacienteEmail.getText());
                
                String cep = jFPacienteCEP.getText().replaceAll("[\\.-]", "").trim();
                cadastroSelecionado.setPatientCEP(cep);
                
                cadastroSelecionado.setPatientAllergies(jTAPacienteAlergia.getText());
                cadastroSelecionado.setIntolerance(jTAPacienteIntolerancia.getText());
                cadastroSelecionado.setPatientJob(jTPacienteProfissao.getText());
                cadastroSelecionado.setPatientMentalHistory(jTAPacienteMental.getText());
                cadastroSelecionado.setPatientPathological(jTAPacienteDoencas.getText());
                cadastroSelecionado.setPatientObservation(jTAPacienteObservacao.getText());

                if (linha == -1) {
                    cadastroDao.inserir(cadastroSelecionado);
                    JOptionPane.showMessageDialog(null, "Novo paciente criado com sucesso.");
                } else {
                    cadastroDao.alterar(cadastroSelecionado);
                    JOptionPane.showMessageDialog(null, "Paciente atualizado com sucesso.");
                }

                Tabelas tabelas = new Tabelas();
                tabelas.atualizaTabelaPaciente(cadastroDao.consultar());

                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao salvar o paciente:\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBPacienteSalvarMouseClicked

