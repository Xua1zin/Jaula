    private void jBUsuarioSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBUsuarioSalvarMouseClicked
        try {
            CadastroUsuarioDao cadastroDao = new CadastroUsuarioDao();
            ArrayList<CadastroUsuarioModel> listaCadastros = cadastroDao.consultar();
            int linha = -1;
            String pacientID = jTUsuarioID.getText();

            boolean isCPFEmpty = false;
            if (jCUsuarioNacionalidade.getSelectedIndex() == 26) {
                String cpf = jFUsuarioCPF.getText().replaceAll("[\\.-]", "").trim();
                isCPFEmpty = cpf.isEmpty();
            }

            if (jTUsuarioID.getText().trim().isEmpty()
                    || jTUsuarioLogin.getText().trim().isEmpty()
                    || jCUsuarioPermissao.getSelectedIndex() < 0
                    || jPUsuarioSenha.getPassword().length == 0
                    || jCUsuarioStatus.getSelectedIndex() < 0
                    || jTUsuarioNome.getText().trim().isEmpty()
                    || jFUsuarioNascimento.getText().trim().isEmpty()
                    || isCPFEmpty
                    || jTCRN.getText().trim().isEmpty()
                    || jCUsuarioRegiao.getSelectedIndex() < 0
                    || jCUsuarioNacionalidade.getSelectedIndex() < 0
                    || jCUsuarioSexo.getSelectedIndex() < 0) {
                JOptionPane.showMessageDialog(null, "Dados Inválidos");
            } else {
                for (int i = 0; i < listaCadastros.size(); i++) {
                    CadastroUsuarioModel cadastro = listaCadastros.get(i);
                    if (pacientID.equals(String.valueOf(cadastro.getUserID()))) {
                        linha = i;
                        break;
                    }
                }

                CadastroUsuarioModel cadastroSelecionado;
                if (linha == -1) {
                    cadastroSelecionado = new CadastroUsuarioModel();
                    int novoID = listaCadastros.stream()
                            .mapToInt(CadastroUsuarioModel::getUserID)
                            .max()
                            .orElse(0) + 1;
                    cadastroSelecionado.setUserID(novoID);
                } else {
                    cadastroSelecionado = listaCadastros.get(linha);
                }

                cadastroSelecionado.setUserStatusName((String) jCUsuarioStatus.getSelectedItem());
                cadastroSelecionado.setPermissionsType((String) jCUsuarioPermissao.getSelectedItem());
                cadastroSelecionado.setUserLogin(jTUsuarioLogin.getText());
                cadastroSelecionado.setUserPassword(new String(jPUsuarioSenha.getPassword()));
                cadastroSelecionado.setUserName(jTUsuarioNome.getText());

                String birthdateText = jFUsuarioNascimento.getText().trim();
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date parsedDate = dateFormat.parse(birthdateText);
                Date sqlDate = new Date(parsedDate.getTime());
                cadastroSelecionado.setUserBirthdate(sqlDate);

                String cpf = jFUsuarioCPF.getText().replaceAll("[\\.-]", "");
                cadastroSelecionado.setUserCPF(cpf);

                String rg = jFUsuarioRG.getText().replaceAll("[\\.-]", "");
                cadastroSelecionado.setUserRG(rg);

                cadastroSelecionado.setSexName((String) jCUsuarioSexo.getSelectedItem());
                cadastroSelecionado.setCRN(jTCRN.getText());
                cadastroSelecionado.setRegionName((String) jCUsuarioRegiao.getSelectedItem());
                cadastroSelecionado.setCountryName((String) jCUsuarioNacionalidade.getSelectedItem());
                cadastroSelecionado.setUserTelefone(jTUsuarioTelefone.getText());
                cadastroSelecionado.setUserEmail(jTUsuarioEmail.getText());
                cadastroSelecionado.setUserObservation(jTUsuarioObservacao.getText());

                if (linha == -1) {
                    cadastroDao.inserir(cadastroSelecionado);
                    JOptionPane.showMessageDialog(null, "Novo usuario criado com sucesso.");
                } else {
                    cadastroDao.alterar(cadastroSelecionado);
                    JOptionPane.showMessageDialog(null, "Usuario atualizado com sucesso.");
                }

                Tabelas tabelas = new Tabelas();
                tabelas.atualizaTabelaUsuario(cadastroDao.consultar());

                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao salvar o Usuário:\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBUsuarioSalvarMouseClicked

